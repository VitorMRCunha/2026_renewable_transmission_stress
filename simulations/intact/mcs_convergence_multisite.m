function sc = make_scenario(name,label,Ps,Pw,inj)
    sc.name = name;
    sc.label = label;
    sc.P_solar_max = Ps;
    sc.P_wind_max = Pw;
    sc.inj = inj;
end

function P = wind_pc(v,vci,vr,vco,Prated)
    P = zeros(size(v));
    r1 = (v>=vci) & (v<=vr);
    P(r1) = Prated*(v(r1).^3-vci^3)/(vr^3-vci^3);
    P(v>vr & v<vco) = Prated;
end

function [mpc,idx_s,idx_w] = add_res_generators(mpc0,cfg)
    mpc = mpc0;
    idx_s = zeros(1,numel(cfg.inj.solar_buses));
    idx_w = zeros(1,numel(cfg.inj.wind_buses));

    for i = 1:numel(cfg.inj.solar_buses)
        Pi = cfg.P_solar_max*cfg.inj.solar_frac(i);
        row = zeros(1,size(mpc.gen,2));
        row(1:10) = [cfg.inj.solar_buses(i),0,0,0.5*Pi,-0.5*Pi,1,100,1,Pi,0];
        mpc.gen = [mpc.gen;row];
        mpc.gencost = [mpc.gencost;zero_cost_row(mpc.gencost)];
        idx_s(i) = size(mpc.gen,1);
    end

    for i = 1:numel(cfg.inj.wind_buses)
        Pi = cfg.P_wind_max*cfg.inj.wind_frac(i);
        row = zeros(1,size(mpc.gen,2));
        row(1:10) = [cfg.inj.wind_buses(i),0,0,0.5*Pi,-0.5*Pi,1,100,1,Pi,0];
        mpc.gen = [mpc.gen;row];
        mpc.gencost = [mpc.gencost;zero_cost_row(mpc.gencost)];
        idx_w(i) = size(mpc.gen,1);
    end
end

function gc = zero_cost_row(gencost)
    ncol = size(gencost,2);
    gc = zeros(1,ncol);
    gc(1)=2;
    gc(4)=ncol-4;
end

function [Psolar_units,Pwind_units] = scenario_available_power(cfg,Gzone,Wzone)
    Ns=size(Gzone,1); T=size(Gzone,2);
    ns=numel(cfg.inj.solar_buses); nw=numel(cfg.inj.wind_buses);
    Psolar_units=zeros(Ns,T,ns); Pwind_units=zeros(Ns,T,nw);
    if numel(cfg.inj.solar_zones)~=ns || numel(cfg.inj.wind_zones)~=nw
        error('Zone mapping length does not match generator count for %s.',cfg.name);
    end
    for i=1:ns
        Psolar_units(:,:,i)=cfg.P_solar_max*cfg.inj.solar_frac(i)*Gzone(:,:,cfg.inj.solar_zones(i));
    end
    for i=1:nw
        Pwind_units(:,:,i)=cfg.P_wind_max*cfg.inj.wind_frac(i)*Wzone(:,:,cfg.inj.wind_zones(i));
    end
end

function X = generate_vector_ar1(Ns,T,R,rho)
    R=(R+R')/2;
    rho=rho(:);
    nz=numel(rho);
    A=diag(rho);
    Q=R-A*R*A';
    R=nearest_corr(R,1e-10);
    Q=project_psd(R-A*R*A',1e-10);
    L0=chol(R,'lower');
    Lq=chol(Q,'lower');
    X=zeros(Ns,T,nz);
    X(:,1,:)=randn(Ns,nz)*L0';
    for t=2:T
        prev=squeeze(X(:,t-1,:));
        eps=randn(Ns,nz)*Lq';
        X(:,t,:)=prev*A'+eps;
    end
end

function M = project_psd(M,tol)
    M=(M+M')/2;
    [V,D]=eig(M);
    d=diag(D);
    d(d<tol)=tol;
    M=V*diag(d)*V';
    M=(M+M')/2;
end

function R = nearest_corr(R,tol)
    R=project_psd(R,tol);
    s=sqrt(diag(R));
    R=R./(s*s');
    R=project_psd((R+R')/2,tol);
    s=sqrt(diag(R));
    R=R./(s*s');
    R=(R+R')/2;
end

function day = run_day_multisite(mpc,Ps_units,Pw_units,idx_s,idx_w, ...
    Pd_frac,Qd0,Ppk_scaled,Ppk,d_t,Prat,Tau,Vmin,Vmax,curt_tol,mpopt)

    T = numel(d_t);
    if isvector(Ps_units), Ps_units=Ps_units(:); end
    if isvector(Pw_units), Pw_units=Pw_units(:); end
    if size(Ps_units,1)~=T, Ps_units=Ps_units'; end
    if size(Pw_units,1)~=T, Pw_units=Pw_units'; end
    nbr = size(mpc.branch,1); nb = size(mpc.bus,1);
    exceed=false(T,nbr,numel(Tau)); vviol=false(T,nb);
    costs=NaN(T,1); losses=NaN(T,1); curtS=NaN(T,1); curtW=NaN(T,1);
    ok=false(T,1); solve_time=NaN(T,1);

    for t=1:T
        mt=mpc; Plt=Ppk_scaled*d_t(t);
        mt.bus(:,3)=Pd_frac*Plt; mt.bus(:,4)=Qd0*(Plt/Ppk);
        for i=1:numel(idx_s), mt.gen(idx_s(i),9)=Ps_units(t,i); end
        for i=1:numel(idx_w), mt.gen(idx_w(i),9)=Pw_units(t,i); end
        ts=tic; r=runopf(mt,mpopt); solve_time(t)=toc(ts); ok(t)=logical(r.success);
        if r.success
            S=max(hypot(r.branch(:,14),r.branch(:,15)), hypot(r.branch(:,16),r.branch(:,17))); V=r.bus(:,8);
            for j=1:numel(Tau), exceed(t,:,j)=(S>Tau(j)*Prat)'; end
            vviol(t,:)=(V<Vmin | V>Vmax)'; costs(t)=r.f;
            losses(t)=sum(r.branch(:,14)+r.branch(:,16));
            curtS(t)=sum(max(0,Ps_units(t,:)'-r.gen(idx_s,2)));
            curtW(t)=sum(max(0,Pw_units(t,:)'-r.gen(idx_w,2)));
            if curtS(t)<curt_tol, curtS(t)=0; end
            if curtW(t)<curt_tol, curtW(t)=0; end
        end
    end
    if all(ok)
        day.exceed_count=uint8(squeeze(sum(exceed,1)));
        day.voltage_violation_count=uint8(sum(vviol,1));
        day.daily_cost=sum(costs); day.daily_loss_MWh=sum(losses);
        day.daily_solar_curt_MWh=sum(curtS); day.daily_wind_curt_MWh=sum(curtW);
        day.max_solar_curt_MW=max(curtS); day.max_wind_curt_MW=max(curtW);
        day.curtailed_hours=nnz((curtS+curtW)>curt_tol);
    else
        day.exceed_count=zeros(nbr,numel(Tau),'uint8');
        day.voltage_violation_count=zeros(1,nb,'uint8');
        day.daily_cost=NaN; day.daily_loss_MWh=NaN;
        day.daily_solar_curt_MWh=NaN; day.daily_wind_curt_MWh=NaN;
        day.max_solar_curt_MW=NaN; day.max_wind_curt_MW=NaN; day.curtailed_hours=NaN;
    end
    day.successful_hours=uint8(nnz(ok));
    day.total_solve_time_s=sum(solve_time,'omitnan');
end

function [estimate,se,lo,hi] = bootstrap_mean(x,B,ci_level)
    x=x(:); x=x(isfinite(x));
    n=numel(x);
    estimate=mean(x);
    if n<2
        se=NaN; lo=NaN; hi=NaN; return;
    end
    boot=zeros(B,1);
    for b=1:B
        idx=randi(n,n,1);
        boot(b)=mean(x(idx));
    end
    se=std(boot,0);
    alpha=(1-ci_level)/2;
    q=prctile(boot,[100*alpha,100*(1-alpha)]);
    lo=q(1); hi=q(2);
end

function [se,lo,hi] = bootstrap_lcp_matrix(daily_counts,T,B,ci_level)
    [n,nbr]=size(daily_counts);
    boot=zeros(B,nbr,'single');
    for b=1:B
        idx=randi(n,n,1);
        boot(b,:)=single(mean(daily_counts(idx,:),1)/T*100);
    end
    se=std(double(boot),0,1);
    alpha=(1-ci_level)/2;
    q=prctile(double(boot),[100*alpha,100*(1-alpha)],1);
    lo=q(1,:); hi=q(2,:);
end

function [estimate,se,lo,hi] = bootstrap_interaction(daily_pp,B,ci_level)
    means=cellfun(@mean,daily_pp);
    estimate=(means(2)-means(1))-(means(4)-means(3));
    boot=zeros(B,1);
    for b=1:B
        m=zeros(4,1);
        for sc=1:4
            x=daily_pp{sc}; n=numel(x);
            m(sc)=mean(x(randi(n,n,1)));
        end
        boot(b)=(m(2)-m(1))-(m(4)-m(3));
    end
    se=std(boot,0);
    alpha=(1-ci_level)/2;
    q=prctile(boot,[100*alpha,100*(1-alpha)]);
    lo=q(1); hi=q(2);
end

function idx=topk_with_ties(x,k,min_abs_value)
    x=x(:);
    eligible=find(isfinite(x) & abs(x)>=min_abs_value);
    if isempty(eligible)
        idx=[];
        return;
    end
    vals=x(eligible);
    vals_sorted=sort(vals,'descend');
    kth_pos=min(k,numel(vals_sorted));
    cutoff=vals_sorted(kth_pos);
    tol=max(1e-12,1e-10*max(1,abs(cutoff)));
    idx=eligible(vals>=cutoff-tol);
end

function pct=jaccard_pct(a,b)
    a=unique(a(:)); b=unique(b(:));
    u=union(a,b);
    if isempty(u)
        pct=NaN;
    else
        pct=100*numel(intersect(a,b))/numel(u);
    end
end

function [estimate,se,lo,hi]=bootstrap_paired_difference(xa,xb,B,ci_level)
    xa=xa(:); xb=xb(:);
    valid=isfinite(xa) & isfinite(xb);
    d=xa(valid)-xb(valid);
    n=numel(d);
    if n==0
        estimate=NaN; se=NaN; lo=NaN; hi=NaN;
        return;
    end
    estimate=mean(d);
    boot=zeros(B,1);
    for b=1:B
        boot(b)=mean(d(randi(n,n,1)));
    end
    se=std(boot,0);
    alpha=(1-ci_level)/2;
    q=prctile(boot,[100*alpha,100*(1-alpha)]);
    lo=q(1); hi=q(2);
end

function idx=find_branch(mpc,fbus,tbus)
    idx=find((mpc.branch(:,1)==fbus & mpc.branch(:,2)==tbus) | ...
             (mpc.branch(:,1)==tbus & mpc.branch(:,2)==fbus),1);
    if isempty(idx)
        error('Branch %d-%d not found.',fbus,tbus);
    end
end
